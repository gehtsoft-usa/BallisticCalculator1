namespace BallisticCalculator.Reticle.Data
{
    /// <summary>
    /// Text anchor position
    /// </summary>
    public enum TextAnchor
    {
        /// <summary>
        /// The text position is at left of the text
        /// </summary>
        Left,
        /// <summary>
        /// The text position is at right of the text
        /// </summary>
        Right,
        /// <summary>
        /// The text should be centered around the text position
        /// </summary>
        Center
    }
}
